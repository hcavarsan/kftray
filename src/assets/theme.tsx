// theme.tsx
import { extendTheme, ThemeConfig } from "@chakra-ui/react";

const config: ThemeConfig = {
  initialColorMode: 'dark',
  useSystemColorMode: false,
};

const theme = extendTheme({
  config,
  styles: {
    global: {
      body: {
        margin: 0,
        backgroundColor: "transparent",
      },
    },
  },
});

export default theme;
